package com.atria.training;

public class Datatypes{
	public static void main(String[] args) {
		int i = 9/2;
		float f = 101/61;
		double d = 10d/6d;
		float f2 = 5/2.5f;
		System.out.println("Value 1 is "+i);
		System.out.println("Value 2 is "+f);
		System.out.println("Value 3 is "+d);
		System.out.println("Value 4 is "+f2);

	}

}
