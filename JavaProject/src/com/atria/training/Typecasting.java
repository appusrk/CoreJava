package com.atria.training;

public class Typecasting {

	public static void main(String[] args) {
		byte b=10;
		int i1=b;
		float f=35.42f;
		int i2=(int)f;
		System.out.println(i1+"\n"+i2);
		
		int i=1254;
		long l= i;
		double d =69.96d;
		byte df=(byte)d;
		System.out.println(l+"\n"+df);
		

	}

}
